@register.simple_tag
def custom_trans(text):
    """Custom translation tag"""
    from django.utils.translation import gettext as _
    translated = _(text)
    if translated == text:
        return text
    return translated

@register.filter
def translate(text):
    """Translation filter"""
    return custom_trans(text)

@register.filter
def auto_translate(text):
    """Auto translation filter - same as translate but with different name"""
    return custom_trans(text)

@register.simple_tag
def t(text):
    """Short translation tag"""
    return custom_trans(text)
