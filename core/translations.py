def get_translation(text, language_code='ar'):
    """
    الحصول على الترجمة للنص المطلوب
    """
    if language_code == 'ar':
        # إذا كانت اللغة عربية، أعيد النص الأصلي إذا كان عربياً، أو ترجمه للعربية
        return TRANSLATIONS.get(text, text)
    else:
        # إذا كانت اللغة إنجليزية، أعيد النص الإنجليزي
        if text in TRANSLATIONS.values():
            # البحث عن المفتاح الإنجليزي للقيمة العربية
            for en_key, ar_value in TRANSLATIONS.items():
                if ar_value == text:
                    return en_key
        return text
